  static List<SignLanguageVideo> getVideosForCategory(String category, SignLanguageType type) {
    switch (category) {
      case 'alphabet':
        return alphabetVideos.where((video) => video.type == type).toList();
      case 'numbers':
        return numberVideos.where((video) => video.type == type).toList();
      case 'common_phrases':
        return phraseVideos.where((video) => video.type == type).toList();
      case 'daily_words':
        return dailyWordsVideos.where((video) => video.type == type).toList();
      default:
        return [];
    }
  }
